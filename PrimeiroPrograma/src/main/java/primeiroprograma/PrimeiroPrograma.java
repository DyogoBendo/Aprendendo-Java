package primeiroprograma; // Pacote, primeiro da hierarquia

public class PrimeiroPrograma { // Classes ficam dentro de pacotes
    public static void main(String[] args){ // Metodos formam as classes, presenca da palavra void indica ser um metodo
        // psvm + tab
        System.out.print("Ola, mundo!"); // System eh uma classe
        System.out.println("Ola, mundo! 2.0"); // sout + tab
        System.out.println("Oie");
    }
}


